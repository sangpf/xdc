package com.newins.model;

import net.sf.json.JSONObject;

import org.springframework.stereotype.Component;

/**
 * @Description 问卷答案model 每个model为1个用户的1个问题的答案
 * @author Guan
 * @time 2016年5月24日 上午8:56:59
 */

@Component(value = "SurveyAnswer")
public class SurveyAnswer {
	int userId, sqnId;
	JSONObject answers;
	int questionType, sqId, preferValue;
	String duration, qAnswer,choice,selfDefine;

	
	public int getPreferValue() {
		return preferValue;
	}

	public void setPreferValue(int preferValue) {
		this.preferValue = preferValue;
	}

	public String getSelfDefine() {
		return selfDefine;
	}

	public void setSelfDefine(String selfDefine) {
		this.selfDefine = selfDefine;
	}

	public String getChoice() {
		return choice;
	}

	public void setChoice(String choice) {
		this.choice = choice;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public int getSqnId() {
		return sqnId;
	}

	public void setSqnId(int sqnId) {
		this.sqnId = sqnId;
	}

	public JSONObject getAnswers() {
		return answers;
	}

	public void setAnswers(JSONObject answers) {
		this.answers = answers;
	}

	public int getQuestionType() {
		return questionType;
	}

	public void setQuestionType(int questionType) {
		this.questionType = questionType;
	}

	public int getSqId() {
		return sqId;
	}

	public void setSqId(int sqId) {
		this.sqId = sqId;
	}

	public String getDuration() {
		return duration;
	}

	public void setDuration(String duration) {
		this.duration = duration;
	}

	public String getqAnswer() {
		return qAnswer;
	}

	public void setqAnswer(String qAnswer) {
		this.qAnswer = qAnswer;
	}

}
