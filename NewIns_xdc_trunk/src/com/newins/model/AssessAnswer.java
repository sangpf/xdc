package com.newins.model;

import net.sf.json.JSONObject;

import org.springframework.stereotype.Component;

/**
 * @Description 测评答案model 每个model为1个用户的1个问题的答案
 * @author Guan
 * @time 2016年5月24日 上午8:56:59
 */

@Component(value = "AssessAnswer")
public class AssessAnswer {
	int userId, aqnId, score;
	JSONObject answers;
	int questionType, aqId;
	String duration, qAnswer;

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public JSONObject getAnswers() {
		return answers;
	}

	public void setAnswers(JSONObject answers) {
		this.answers = answers;
	}

	public int getQuestionType() {
		return questionType;
	}

	public void setQuestionType(int questionType) {
		this.questionType = questionType;
	}

	public int getAqnId() {
		return aqnId;
	}

	public void setAqnId(int aqnId) {
		this.aqnId = aqnId;
	}

	public int getAqId() {
		return aqId;
	}

	public void setAqId(int aqId) {
		this.aqId = aqId;
	}

	public String getDuration() {
		return duration;
	}

	public void setDuration(String duration) {
		this.duration = duration;
	}

	public String getqAnswer() {
		return qAnswer;
	}

	public void setqAnswer(String qAnswer) {
		this.qAnswer = qAnswer;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

}
