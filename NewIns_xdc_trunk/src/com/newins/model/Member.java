package com.newins.model;
/**
 * 个人会员实体类
 * @author Zhangwenhao
 *
 */
public class Member {
	private int userId;//用户id
	private String bTime;//开通时间
	private String eTime;//结束时间
	//封装属性
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getbTime() {
		return bTime;
	}
	public void setbTime(String bTime) {
		this.bTime = bTime;
	}
	public String geteTime() {
		return eTime;
	}
	public void seteTime(String eTime) {
		this.eTime = eTime;
	}
	/**
	 * 全参构造
	 * @param userId
	 * @param bTime
	 * @param eTime
	 */
	public Member(int userId, String bTime, String eTime) {
		super();
		this.userId = userId;
		this.bTime = bTime;
		this.eTime = eTime;
	}
	/**
	 * 空参构造
	 */
	public Member() {
		super();
	}
	
	
}
