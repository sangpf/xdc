package com.newins.model;

import java.io.Serializable;

/**
 * 库润采集信息实体类
 * @author Zhangwenhao
 *
 */
public class KuRunGather implements Serializable {
	private int userId;//用户Id
	private String city;//城市
	private int age;//年龄
	private String province;//省份
	private int sex;//性别
	
	//封装属性
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
	
	
	/**
	 * 空参构造
	 */
	public KuRunGather() {
		super();
	}
	/**
	 * 全参构造
	 */
	public KuRunGather(int userId, String city, int age, String province,
			int sex) {
		super();
		this.userId = userId;
		this.city = city;
		this.age = age;
		this.province = province;
		this.sex = sex;
	}
	
	
}
