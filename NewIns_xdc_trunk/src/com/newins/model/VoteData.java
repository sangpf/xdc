package com.newins.model;

import java.util.List;

import org.springframework.stereotype.Component;

/**@Description  
 * @author MaNia_chAng
 * @time 2016年6月20日 下午4:52:44
 */
@Component(value = "VoteData")
public class VoteData {
	String success,comments,userAnswer;
	int optionNum,totalAmount,participant;
	List<VoteOptionData> voteOptionData;
	List<String> optionDes;
	public String getSuccess() {
		return success;
	}
	public void setSuccess(String success) {
		this.success = success;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public int getOptionNum() {
		return optionNum;
	}
	public void setOptionNum(int optionNum) {
		this.optionNum = optionNum;
	}
	public int getTotalAmount() {
		return totalAmount;
	}
	public void setTotalAmount(int totalAmount) {
		this.totalAmount = totalAmount;
	}
	public List<VoteOptionData> getVoteOptionData() {
		return voteOptionData;
	}
	public void setVoteOptionData(List<VoteOptionData> voteOptionData) {
		this.voteOptionData = voteOptionData;
	}
	public List<String> getOptionDes() {
		return optionDes;
	}
	public void setOptionDes(List<String> optionDes) {
		this.optionDes = optionDes;
	}
	public int getParticipant() {
		return participant;
	}
	public void setParticipant(int participant) {
		this.participant = participant;
	}
	public String getUserAnswer() {
		return userAnswer;
	}
	public void setUserAnswer(String userAnswer) {
		this.userAnswer = userAnswer;
	}
	

	
	

}
